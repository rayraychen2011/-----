"""
敲磚塊遊戲 - 主程式入口點\n
\n
一個使用 Python 和 Pygame 開發的經典敲磚塊遊戲，具有豐富的視覺效果和音效。\n
\n
操作說明：\n
- 滑鼠移動：控制底板位置\n
- 空白鍵/右鍵：發射球\n
- 左 Shift：按住時球會加速移動\n
- ESC：退出遊戲\n
- R 鍵：重新隨機化磚塊漸層顏色\n
- Q 鍵：顯示/隱藏操作說明\n
- 左鍵：切換底板自動模式\n
- 中鍵：切換物理計算面板顯示\n
"""

from src.game.game_logic import BrickBreakerGame

######################主程式######################

def main():
    """
    主程式入口點 - 啟動敲磚塊遊戲\n
    \n
    此函數負責：\n
    1. 建立遊戲控制器實例\n
    2. 啟動遊戲主循環\n
    3. 處理遊戲運行時的錯誤\n
    \n
    錯誤處理：\n
    - 捕捉所有例外並顯示錯誤訊息\n
    - 印出完整的錯誤追蹤資訊方便除錯\n
    """
    try:
        # 建立遊戲控制器，這會初始化所有遊戲資源
        game = BrickBreakerGame()
        # 開始遊戲主循環，直到玩家關閉視窗
        game.run()
    except Exception as e:
        # 遊戲運行中出現任何錯誤都會在這裡被捕捉
        print(f"遊戲運行時發生錯誤: {e}")
        import traceback
        # 顯示完整的錯誤追蹤，幫助開發者找出問題
        traceback.print_exc()

# 直接呼叫主函數，不使用 if __name__ == "__main__" 慣例
main()
